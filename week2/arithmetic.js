b = a++ //post-fix operator:    b=a=10; a++=a+1=10+1=11
c = ++a //pre-fix operator:     ++a=a+1=11+1=12;  c=a=12
console.log("a = " + a)  //12
console.log("b = " + b)  //10
console.log("c = " + c)  //12

